            <p class="c-center c-font-uppercase c-font-bold">This Policy Statement explains how your personal information will be collected and managed by RMIT.</p>
			<p class="c-center">RMIT values the privacy of every individual and is committed to handling personal and health information in accordance with the privacy principles contained in the Privacy and Data Protection Act 2014 (Vic), the Health Records Act 2001 (Vic) and other relevant legislation.</p>
            <p class="c-center">Transparency and fairness for the management of personal, health and sensitive information at RMIT and all its operations underpins this policy statement.  Privacy and security measures protect against misuse, loss and unauthorised disclosure of personal information.</p>
            <h4 class="c-left c-font-uppercase c-font-bold">1.  Collection of personal information</h4>
            <p>RMIT collects personal and health information as necessary for its core functions, including for educational, research, community and commercial purposes. RMIT collects personal information about staff, students, research participants, volunteers, RMIT club and gym members, and other members of the public.
            The type of personal or health information RMIT collects depends on the nature of the contact and interaction.

            Where lawful and practicable, an individual may remain anonymous when interacting with RMIT. This means that no identifying details will be collected.

            RMIT may collect sensitive information in certain limited circumstances. When we collect sensitive information, all reasonable steps are taken to explain:

            Why the sensitive information is being collected;
            What will be done with this information; and
            The main consequences (if any) for an individual if all or part of the information is not provided to RMIT.
            Some areas of RMIT may have additional specific privacy information or statements that further explain particular information management practices.</p>

            <h4 class="c-left c-font-uppercase c-font-bold">2.  Using personal information</h4>

            <p>RMIT uses and provides, personal or health information to other people or organisations, for the purposes it was collected.</p>

            <p>Typical collections and uses of information include, but are not limited to:</p>

            <li>Application for RMIT services</li>
            <li>Enquiries</li>
            <li>Enrolling students</li>
            <li>Details of staff and volunteers</li>
            <li>Research data</li>
            <li>Conducting marketing campaigns</li>
            <li>Budgeting</li>
            <li>Conducting field trips</li>
            <li>Maintaining information technology facilities</li>
            <li>Liaising with the Student Union</li>
            <li>Transacting with other RMIT entities</li>
            <li>Student services</li>
            <li>Assessing students' work.</li>
            <p>Occasionally, RMIT may be authorised by law to use or provide personal or health information to others for other purposes. In some cases an individual’s consent will be sought to use or provide personal information to others.</p>

            <p>RMIT will only assign or adopt a unique identifier (e.g. student or employee numbers) for an individual if it is necessary, authorised by law or with consent.  RMIT is committed to ensuring that any transfer of personal or health information outside of Victoria is in accordance with law.</p>

            <h4 class="c-left c-font-uppercase c-font-bold"> 3.  Ensuring that information is accurate and up-to-date</h4>

            <p>RMIT takes reasonable steps to ensure that personal and heath information held is accurate, complete and up-to-date.
            RMIT relies on individuals to provide accurate and current information in the first instance, and to notify when circumstances or details change.</p>

            <h4 class="c-left c-font-uppercase c-font-bold"> 4.  Storage and protection of information</h4>

            <p>All areas of RMIT have security measures to protect personal and health information from misuse, loss, unauthorised access or disclosure.
            Stored information is also archived in accordance with the Public Records Act 1973 (Vic), which determines when information should be retained or disposed of.</p>
            <p>Personal information may be stored in hard copy documents, as electronic data, or in RMIT software or systems. Some of the ways RMIT seeks to protect personal information include:</p>

            <li>Privacy process and the protection of information</li>
            <li>Document storage and data security processes</li>
            <li>Security measures for access to RMIT computer systems</li>
            <li>Controlling access to RMIT premises</li>
            <li>Web site protection measures</li>
            <p>Different areas within RMIT have different storage systems in place, which depend upon a variety of factors, including the level of access required by staff and the sensitivity of the information.</p>

            <h4 class="c-left c-font-uppercase c-font-bold">5.  Access to information held by RMIT</h4>

            <p>An individual may ask for access to their personal or health information, without having to make a formal request under the Freedom of Information Act 1982 (FOI Act), where appropriate.
            In some situations, access will not be appropriate, and an individual is required to make a formal FOI request for example, if a third party’s privacy is involved).
            The relevant processes for requesting access to information include directly contacting the area of RMIT that has the information in the first instance followed by 
            making the request pursuant to Freedom of Information legislative guidance. Information of Freedom of Information process at RMIT, please contact https://www.rmit.edu.au/about/governance-and-management/governance/freedom-of-information.</p>

